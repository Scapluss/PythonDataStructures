Fav_fruits = ["Lychee", "Mango", "Pineapple", "Guava", "Orange"]

print(f"Original list: {Fav_fruits}")
Fav_fruits.append("Grapes")
print(f"After adding a fruit: {Fav_fruits}")
Fav_fruits.remove("Guava")
print(f"After removing a fruit: {Fav_fruits}")
Fav_fruits.reverse()
print(f"Reversed list: {Fav_fruits}")
